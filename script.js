const openModalBtn = document.getElementById('openModalBtn');
const modal = document.getElementById('modal');
const overlay = document.getElementById('overlay');
const changeColorBtn = document.getElementById('changeColorBtn');
const colorInput = document.getElementById('colorInput');

openModalBtn.addEventListener('click', () => {
  modal.style.display = 'block';
  overlay.style.display = 'block';
});

overlay.addEventListener('click', () => {
  modal.style.display = 'none';
  overlay.style.display = 'none';
});

changeColorBtn.addEventListener('click', () => {
  const allowedColors = ['red', 'blue', 'green', 'black', 'white'];
  const userColor = colorInput.value.trim().toLowerCase();
  
  if (allowedColors.includes(userColor)) {
    document.body.style.backgroundColor = userColor;
    modal.style.display = 'none';
    overlay.style.display = 'none';
  } else {
    alert('Invalid color! Please enter one of the following: red, blue, green, black, white.');
  }
});
